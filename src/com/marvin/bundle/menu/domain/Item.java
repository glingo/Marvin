package com.marvin.bundle.menu.domain;

public class Item {
    
    private String name;

    private String command;

    public Item() {
    }
    
    public String getCommand() {
        return command;
    }

    public void setCommand(String command) {
        this.command = command;
    }
    
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
